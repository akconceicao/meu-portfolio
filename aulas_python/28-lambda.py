# função de potencia de numeros
def power(num): return num ** 2

# função par
def pair(x): return x % 2 == 0

# função q divide um p outro
def divNum(x, y): return x/y

# funcao q inverte uma string
def reverse(s): return s[::-1]

print(power(5))
print(power(9))
print(pair(27))
print(pair(30))
print(divNum(10,2))
print(divNum(6,2))
print(reverse("Python"))

